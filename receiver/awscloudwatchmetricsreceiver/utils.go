func PrintJson(data interface{}) {
	if b, err := json.Marshal(data); err != nil {
		log.Printf("error marshalling data: %v", err)
	} else {
		fmt.Println(string(b))
	}
}

func FetchOtelUnit(sUnit types.StandardUnit) string {
	if otelUnit, ok := otelMaps[string(sUnit)]; ok {
		return otelUnit
	}
	return "1"
}

func FetchStandardUnit(namespace, metricName string) types.StandardUnit {
	if metricMap, ok := MetricsUnitMap[namespace]; ok {
		if unit, ok := metricMap[metricName]; ok {
			return unit
		}
	}
	return types.StandardUnitNone
}

